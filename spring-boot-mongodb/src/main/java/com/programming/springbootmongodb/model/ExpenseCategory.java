package com.programming.springbootmongodb.model;
/**
 * Enumeration representing different categories for expenses.
 *
 * - ENTERTAINMENT: Expenses related to entertainment activities.
 * - GROCERIES: Expenses related to grocery shopping.
 * - RESTAURANTS: Expenses incurred at restaurants.
 * - UTILITIES: Expenses related to utility bills.
 * - MISC: Miscellaneous expenses that don't fall into specific categories.
 */

public enum ExpenseCategory {


    ENTERTAINMENT, GROCERIES, RESTAURANTS, UTILITIES, MISC
}
